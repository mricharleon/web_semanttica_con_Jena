    private void jMenu1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jMenu1MouseClicked
        // TODO add your handling code here:
        JFileChooser fc = new JFileChooser();
        int respuesta = fc.showOpenDialog(this);
        if (respuesta == JFileChooser.APPROVE_OPTION) {
            //Crear un objeto File con el archivo elegido
            File archivoElegido = fc.getSelectedFile();
            //Mostrar el nombre del archvivo en un campo de texto
            texto.setText("");
            texto.setText(archivoElegido.getName());
            //texto.setEditable(false);
            String y=archivoElegido.getAbsolutePath();///obtengo la ruta del archivo elegido para enviarlo como parametro
            leerOntologias o1 = new leerOntologias();
            //o1.leerClasesySub(y);
            texto2.setText("");
            individuos.setText("");
            propiedades.setText("");
            texto2.setText(o1.leerClasesySub(y));
            try {
                individuos.setText(o1.devolverIndividos(y));
                propiedades.setText(o1.leerP(y));
                //o1.leerP(y);
            } catch (FileNotFoundException ex) {
                Logger.getLogger(interfaz.class.getName()).log(Level.SEVERE, null, ex);
            }
            individuos.setEditable(false);
            texto2.setEditable(false);
            texto.setEditable(false);
            propiedades.setEditable(false);
        }
    }//GEN-LAST:event_jMenu1MouseClicked

